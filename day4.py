
# Arithmetic operations

print(15+6)  # Addition ( For addition of two numbers )
print(15-6)  # Subtraction ( For subtraction of two numbers )

print(15*6)  # Multiplication ( For multiplication of two numbers )
print(15**3) # Exponential ( for eg. 15*15*15)

print(15/6)  # Divide ( For division of two numbers )
print(15//6) # Floor Division ( to get the division value without decimal )

print(15%6)  # Modulo ( For modulo of two numbers )It gives the value of reminder which we get after division
