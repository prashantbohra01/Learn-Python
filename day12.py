#_______For_Loops________________

# Example 1:
a="Prashant"
for i in a:  # for loop in string
    print(i)

#Example 2:
color=["red", "green", "blue", "yellow"]

for x in color:
    print(x)
                     # for loop used for Lists
    for y in x:
     print(y)

#Example 3:

for n in range(5):
   print(n)             #this will start from 0

for k in range(0,7):
   print(k+1)           #this will start from 1


'''
   A for loop is used for iterating over a sequence 
   (that is either a list, a tuple, a dictionary, a set, or a string).
'''